public class day3operator {

    public static void main(String[] args) {

        //1.Arithmatic operator

        int a=200, b=100;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a%b);
        System.out.println(a/b);
        System.out.println("Sum of a and b:"+(a+b));
        System.out.println("Sum of a and b:"+(a-b));
        System.out.println("Sum of a and b:"+(a*b));
        System.out.println("Sum of a and b:"+(a/b));
        System.out.println("Sum of a and b:"+(a%b));

        //2. Relational Operator

        System.out.println(a<b);
        System.out.println(a>b);
        System.out.println(a>=b);
        System.out.println(a<=b);
        System.out.println(a!=b);

    }
}
